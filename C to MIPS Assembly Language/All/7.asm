if(A[6]==C[6])
f[8]=g[8]+c[5]+A[2];
else
f[8]=g[5]+c[3];

A এর base address $s0 তে
c এর base address $s2 তে
g এর base address $s1 এ
f এর base address $s3 তে

# ১. কন্ডিশন পরীক্ষা (if A[6] == C[6])
lw   $t0, 24($s0)       # $t0 = A[6] লোড করা হলো
lw   $t1, 24($s2)       # $t1 = C[6] লোড করা হলো
bne  $t0, $t1, Else     # যদি A[6] != C[6] হয়, তবে Else লেবেলে চলে যাও

# ২. If ব্লকের কাজ: f[8] = g[8] + c[5] + A[2]
lw   $t2, 32($s1)       # $t2 = g[8]
lw   $t3, 20($s2)       # $t3 = c[5]
lw   $t4, 8($s0)        # $t4 = A[2]

add  $t5, $t2, $t3      # $t5 = g[8] + c[5]
add  $t5, $t5, $t4      # $t5 = (g[8] + c[5]) + A[2]
sw   $t5, 32($s3)       # f[8] = $t5 (মেমোরিতে স্টোর করা হলো)
j    Exit               # Else ব্লক স্কিপ করার জন্য Exit-এ জাম্প করো

Else:
# ৩. Else ব্লকের কাজ: f[8] = g[5] + c[3]
lw   $t2, 20($s1)       # $t2 = g[5]
lw   $t3, 12($s2)       # $t3 = c[3]

add  $t5, $t2, $t3      # $t5 = g[5] + c[3]
sw   $t5, 32($s3)       # f[8] = $t5 (মেমোরিতে স্টোর করা হলো)

Exit:
